    private void resultButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_resultButtonActionPerformed

        Graphics g = rW.getGraphics();
        Graphics2D g2d = (Graphics2D) g;

        int[] coords_x = convertIntegers(xNY.polyX);
        int[] coords_y = convertIntegers(xNY.polyY);

        if (typeSelectorBox.getSelectedIndex() == 1) {
            switch (styleSelector.getSelectedIndex()) {
                case 0:
                    rW.dotDrawer(g, x, y, 5, 5);
                    break;
                case 1:
                    rW.dotDrawer(g, x, y, 11, 11);
                    break;
                case 2:
                    rW.dotDrawer(g, x, y, 22, 22);
                    break;

            }
        }
        if (typeSelectorBox.getSelectedIndex() == 2) {
            switch (styleSelector.getSelectedIndex()) {
                case 0:
                    rW.tLineDrawer(g, coords_y, coords_y);
                    break;
                case 1:
                    rW.bLineDrawer(g2d, coords_y, coords_y, 25);
                    break;
                case 2:
                    rW.dLineDrawer(g2d, coords_y, coords_y);
                    break;

            }
        }
        if (typeSelectorBox.getSelectedIndex() == 3) {
            switch (styleSelector.getSelectedIndex()) {
                case 0:
                    rW.sPolyDrawer(g, coords_x, coords_y);
                    break;
                case 1:
                    rW.cPolyDrawer(g, coords_x, coords_y);
                    break;
                case 2:
                    rW.fPolyDrawer(g, coords_x, coords_y);
                    break;

            }
        }
        //clearing arraylists for future drawings
        xNY.polyX.clear();
        xNY.polyY.clear();
    }//GEN-LAST:event_resultButtonActionPerformed

